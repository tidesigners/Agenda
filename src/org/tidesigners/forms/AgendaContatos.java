    public void excluirContato() throws SQLException {
        int resp = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este contato?", "Confirma√ß√£o", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_NO_OPTION) {
            ContatoDAO dao;

            dao = new ContatoDAO();
            dao.remove(contatos.get(jTabela.getSelectedRow()));
            mostraPesquisa(contatos);

        }
    }

    public void listarContatos() throws SQLException {
        ContatoDAO dao = new ContatoDAO();
        contatos = dao.getLista("%" + jTPesquisar.getText() + "%");
        mostraPesquisa(contatos);

    }

    public void cadastro() throws SQLException {
        try {
            Contato c1 = new Contato();
            c1.setNome(jTNome.getText());
            c1.setEndereco(jTEndereco.getText());
            c1.setEmail(jTEmail.getText());
            c1.setFone(jTFone.getText());
            c1.setSexo(jTSexo.getText());

            ContatoDAO dao = new ContatoDAO();
            dao.adiciona(c1);
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(null, "Problemas ao inserir contato" + ex);
        }
    }

    private void desabilitaDados() {
        jTid.setEditable(false);
        jTNome.setEditable(false);
        jTEndereco.setEditable(false);
        jTEmail.setEditable(false);
        jTFone.setEditable(false);
        jTSexo.setEditable(false);
    }

    public void habilitaDados() {
        jTNome.setEditable(true);
        jTEndereco.setEditable(true);
        jTEmail.setEditable(true);
        jTFone.setEditable(true);
        jTSexo.setEditable(true);
    }

    public boolean verificaDados() {
        if (!jTNome.getText().equals("")
                && !jTEmail.getText().equals("")
                && !jTFone.getText().equals("")) {

            return false;

        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os Campos.");
            return true;
        }
    }

    private void jTabelalinhaSelecionada(JTable tabela) {
        if (jTabela.getSelectedRow() != -1) {
            jTid.setText(String.valueOf(contatos.get(tabela.getSelectedRow()).getId()));
            jTNome.setText(contatos.get(tabela.getSelectedRow()).getNome());
            jTEndereco.setText(contatos.get(tabela.getSelectedRow()).getEndereco());
            jTEmail.setText(contatos.get(tabela.getSelectedRow()).getEmail());
            jTFone.setText(contatos.get(tabela.getSelectedRow()).getFone());
            jTSexo.setText(contatos.get(tabela.getSelectedRow()).getSexo());
        } else {
            jTid.setText("");
            jTNome.setText("");
            jTEndereco.setText("");
            jTEmail.setText("");
            jTFone.setText("");
            jTSexo.setText("");

        }
    }

